#include <iostream>
using namespace std;
int main() {
    setlocale(LC_ALL, "");
    int n = 10; 
    int fib[10]; 

    
    fib[0] = 0;
    fib[1] = 1;

    
    cout << "Первые " << n << " чисел Фибоначчи:" << endl;

    for (int i = 2; i < n; ++i) {
        fib[i] = fib[i - 1] + fib[i - 2];
    }

    for (int i = 0; i < n; ++i) {
        cout << fib[i] << " ";
    }

    return 0;
}

